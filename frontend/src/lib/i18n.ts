export function t(key: keyof typeof messages.zh) {
  return messages[currentLocale.value][key] ?? messages.zh[key];
}

export function tf(key: keyof typeof messages.zh, values: Record<string, string | number>) {
  return Object.entries(values).reduce(
    (text, [token, value]) => text.replace(new RegExp(`\\{${token}\\}`, 'g'), String(value)),
    t(key),
  );
}
